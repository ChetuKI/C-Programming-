// Program to understand Relational operator in C

#include<stdio.h>
int main()
{
    int a = 5 , b = 5 ;
    printf("%d \n",a==b);   // 1--True     0--false
    printf("%d \n",a!=b); 
    printf("%d \n",a<b); 
    printf("%d \n",a<=b); 
    printf("%d \n",a>b); 
    printf("%d \n",a>=b); 
}